
package operator;


public class Operator {

    public static void main(String[] args) {
       
        int x=8,y=9;
        System.out.println("Arithematic Opretors Are ");
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
        System.out.println("Unary Operators Are");
        System.out.println(x++);
        System.out.println(++x);
        System.out.println(x--);
        System.out.println(--x);
        System.out.println(x=+1);
        System.out.println(x=-1);
        System.out.println("Comparision Operators Are");
        System.out.println(x<y);
        System.out.println(x>y);
        System.out.println(x==y);
        System.out.println(x<=y);
        System.out.println(x>=y);
        System.out.println(x!=y);
        
        System.out.println("Assignment Opreators Are");
        System.out.println(x=y);
        System.out.println(x+=1);
        System.out.println(x-=1);
      
       
        
        
    }
    
}
